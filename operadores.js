/*
    Operadores

    Nos permitem fazer operações matemáticas, de comparação e lógicas.
*/

// Aritméticos
console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(8/4);

// Comparação
console.log(2 == 2);
console.log(2 == 3);

// Lógicas

// Igual ==
console.log(2 == 2);
console.log(5 == 2);
console.log(2 == "2"); //retorna true porque os valores aqui são iguais, independente do tipo

// Não igual !=
console.log(2 != 2);
console.log(5 != 2);

// Igual estrito === (retorna verdadeiro se o valor e também o tipo forem iguais)
console.log(2 === "2");

// Não igual estrito !==
console.log(3 !== "3");

// Maior que >
console.log(2>3);
console.log(2>1);

// Menor que <
console.log(2<3);
console.log(2<1);

// Maior igual >=
console.log(2>=3);
console.log(2>=2);
console.log(2>=1);

// Menor igual <=
console.log(2<=3);
console.log(2<=2);
console.log(2<=1);