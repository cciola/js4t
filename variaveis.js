/*
    Variáveis

    Variável é um local na memória do computador onde podemos alocar valores, que poderão ser alterados durante a execução do software.
    Por isso, chamamos de variáveis.

    Assim como o Ruby, o javascript têm uma tipagem dinâmica, ou seja, atribui o tipo quando recebe o valor.
*/

var numero = 10;
console.log(numero);

// typeof é responsável por trazer o tipo da variável
console.log(typeof numero);

var nome = "Carol";
console.log(typeof nome);

var n1 = 10;
var n2 = 20;

n1 = "1" + 5;

console.log(n1 + n2)